package com.qmm.entity.font;

public class Score {
    private Integer id;

    private Integer mathScore;

    private Integer engishScore;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMathScore() {
        return mathScore;
    }

    public void setMathScore(Integer mathScore) {
        this.mathScore = mathScore;
    }

    public Integer getEngishScore() {
        return engishScore;
    }

    public void setEngishScore(Integer engishScore) {
        this.engishScore = engishScore;
    }
}